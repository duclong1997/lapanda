    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
           ServletContext context = getServletContext();
        String type = request.getParameter("search");
        String page = request.getParameter("page");
        String pageSize = request.getParameter("pageSize");
        HttpSession session = request.getSession();
         // sesion for compare page
        session.setAttribute("page", Integer.parseInt(page));
        session.setAttribute("valueSearch", "where nameProduct like '%"+type+"%'");
        session.setAttribute("valueType", type);
        ArrayProduct array = new ArrayProduct();
        array.setPage(Integer.parseInt(page));
        array.setPageSize(Integer.parseInt(pageSize));
        array.setSearch("where nameProduct like '%"+type+"%'");
        try {
            ArrayList<Product> pro = array.select("where nameProduct like '%"+type+"%'");
             context.setAttribute("searcher",pro);
             response.sendRedirect("SearchProduct.jsp");
        } catch (Exception ex) {
            Logger.getLogger(SearchType.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

